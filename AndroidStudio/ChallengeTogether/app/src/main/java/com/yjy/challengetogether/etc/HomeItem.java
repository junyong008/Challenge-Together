package com.yjy.challengetogether.etc;

public class HomeItem {

    private String roomidx, icon, title, content, recentstarttime, currentusernum, maxusernum, roompasswd;
    private long endtime;


    public String getRoomidx() {
        return roomidx;
    }

    public void setRoomidx(String roomidx) {
        this.roomidx = roomidx;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getRecentStartTime() {
        return recentstarttime;
    }

    public void setRecentStartTime(String recentstarttime) {
        this.recentstarttime = recentstarttime;
    }

    public long getEndTime() {
        return endtime;
    }

    public void setEndTime(long endtime) {
        this.endtime = endtime;
    }

    public String getCurrentUserNum() {
        return currentusernum;
    }

    public void setCurrentUserNum(String currentusernum) {
        this.currentusernum = currentusernum;
    }

    public String getMaxUserNum() {
        return maxusernum;
    }

    public void setMaxUserNum(String maxusernum) {
        this.maxusernum = maxusernum;
    }

    public String getRoomPasswd() {
        return roompasswd;
    }

    public void setRoomPasswd(String roompasswd) {
        this.roompasswd = roompasswd;
    }
}
