package com.yjy.challengetogether.etc;

public class NotificationItem {

    private String useralarmidx, title, content, createdate, type, linkidx, isview;

    public String getUseralarmidx() {
        return useralarmidx;
    }

    public void setUseralarmidx(String useralarmidx) {
        this.useralarmidx = useralarmidx;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getCreatedate() {
        return createdate;
    }

    public void setCreatedate(String createdate) {
        this.createdate = createdate;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getLinkidx() {
        return linkidx;
    }

    public void setLinkidx(String linkidx) {
        this.linkidx = linkidx;
    }

    public String getIsview() {
        return isview;
    }

    public void setIsview(String isview) {
        this.isview = isview;
    }
}
